namespace CodeFuller.Library.Core.Enums
{
	/// <summary>
	/// Enumeration of possible icons that are displayed in MessageBox.
	/// </summary>
	public enum ShowMessageBoxIcon
	{
		/// <summary>
		/// Undefined default value.
		/// </summary>
		None,

		/// <summary>
		/// No icon is displayed.
		/// </summary>
		NoImage,

		/// <summary>
		/// Information icon is displayed.
		/// </summary>
		Information,

		/// <summary>
		/// Warning icon is displayed.
		/// </summary>
		Warning,

		/// <summary>
		/// Error icon is displayed.
		/// </summary>
		Error,

		/// <summary>
		/// Question icon is displayed.
		/// </summary>
		Question,

		/// <summary>
		/// Exclamation icon is displayed.
		/// </summary>
		Exclamation,
	}
}
