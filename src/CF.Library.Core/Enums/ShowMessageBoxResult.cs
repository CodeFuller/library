namespace CF.Library.Core.Enums
{
	/// <summary>
	/// Enumeration of possible outcomes of showing message box.
	/// </summary>
	public enum ShowMessageBoxResult
	{
		/// <summary>
		/// Default undefined value.
		/// </summary>
		None,

		/// <summary>
		/// Message box returned no result.
		/// </summary>
		NoResult,

		/// <summary>
		/// User pressed OK button.
		/// </summary>
		Ok,

		/// <summary>
		/// User pressed Cancel button.
		/// </summary>
		Cancel,

		/// <summary>
		/// User pressed Yes button.
		/// </summary>
		Yes,

		/// <summary>
		/// User pressed No button.
		/// </summary>
		No,
	}
}
